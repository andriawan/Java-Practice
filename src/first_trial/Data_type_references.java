package first_trial;

public class Data_type_references {

	/**
	 * type of data in java
	 */
	public static void main(String[] args) {
		// type = byte  Allocated Memory = 8  Bits Range = -128 to 127
		byte x = -90;
		System.out.println(x);

		// type = short Allocated Memory = 16 Bits Range = -32.768 to 32.767
		short c = 32767;
		System.out.println(c);
		
		// type = integer or int Allocated Memory = 32 Bits Range = -2.147.483.648 to 2.147.483.647
		int z   = 333333333;
		System.out.println(z);
		
		// type = long  Allocated Memory = 64 Bits Range = -9.223.372.036.854.775.808 to 9.223.372.036.854.775.807
		long s  = 34344344;
		System.out.println(s);
		
		// type = float Allocated Memory = 32 Bits Range = 1.4e-045 to 3.4e+038
		float g = 666666666;
		System.out.println(g);
		
		// type = double Allocated Memory = 64 Bits Range = 4.9e-324 to 1.8e + 308
		double h = 777777.777;
		System.out.println(h);
		
		// type = char Allocated Memory = 16 Bits Range = 'y'
		char k  = 99;
		System.out.println(k);
		
		// type = boolean Allocated Memory = 1 Bits Range = true or false
		boolean m = true;
		System.out.println(m);
		
	}

}
